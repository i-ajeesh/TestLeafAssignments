package week1.day2;

public class IfElseActivity {

	public static void main(String[] args) {
		int mark = 360;
		
		if(mark>400) {
			System.out.println("Mark is less than 400");
		}
		else if (mark>320) {
			System.out.println("Mark is more than 320");
		}
		else {
			System.out.println("Mark is" +mark);
		}
	}
	

}
